  private void jButtonAtualizarDespesaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarDespesaActionPerformed
    // TODO add your handling code here:
    if (idSelecionado == -1) {
      JOptionPane.showMessageDialog(this, "Selecione uma despesa na tabela.");
      return;
    }

    String novaDescricao = jTextArea1.getText().trim();

    if (novaDescricao.isEmpty()) {
      JOptionPane.showMessageDialog(this, "Digite uma descrição para atualizar.");
      return;
    }

    String msg = controller.atualizarTipo(idSelecionado, novaDescricao);
    JOptionPane.showMessageDialog(this, msg);

    carregarTabela();
    limparCampos();
  }//GEN-LAST:event_jButtonAtualizarDespesaActionPerformed

