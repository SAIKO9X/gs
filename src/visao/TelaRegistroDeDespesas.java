  private void carregarTabela() {
    try {
      List<TipoDeDespesa> lista = controller.listar();

      DefaultTableModel modelo = (DefaultTableModel) jTableGreedDespesas.getModel();
      modelo.setRowCount(0);

      for (TipoDeDespesa t : lista) {
        modelo.addRow(new Object[]{
          t.getIdTipoDeDespesa(),
          t.getDescricao()
        });
      }

    } catch (Exception e) {
      JOptionPane.showMessageDialog(null, "Erro ao carregar tabela: " + e.getMessage());
    }
  }

  private void limparCampos() {
    jTextArea1.setText("");
    idSelecionado = -1;
    jTableGreedDespesas.clearSelection();
  }
