  private void jButtonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarActionPerformed
    try {

      if (modo.equals("CADASTRAR")) {
        String desc = jTextArea1.getText().trim();
        if (desc.isEmpty()) {
          JOptionPane.showMessageDialog(this, "Digite uma descrição.");
          return;
        }
        JOptionPane.showMessageDialog(this, controller.cadastrarTipo(desc));
      } else if (modo.equals("ATUALIZAR")) {
        String desc = jTextArea1.getText().trim();
        if (idSelecionado == -1) {
          JOptionPane.showMessageDialog(this, "Selecione uma despesa.");
          return;
        }
        JOptionPane.showMessageDialog(this, controller.atualizarTipo(idSelecionado, desc));
      } else if (modo.equals("REMOVER")) {
        if (idSelecionado == -1) {
          JOptionPane.showMessageDialog(this, "Selecione uma despesa.");
          return;
        }
        JOptionPane.showMessageDialog(this, controller.removerTipo(idSelecionado));
      } else if (modo.equals("LISTAR")) {
        carregarTabela();
      } else {
        JOptionPane.showMessageDialog(this, "Escolha uma opção primeiro.");
        return;
      }

      carregarTabela();
      jTextArea1.setText("");
      jTextArea1.setEnabled(false);
      idSelecionado = -1;
      modo = "";

    } catch (Exception e) {
      JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
    }
  }//GEN-LAST:event_jButtonConfirmarActionPerformed

